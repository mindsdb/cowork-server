from pathlib import Path
from pydantic import Field

from cowork.common.settings import Settings


class AntonHarnessSettings(Settings):
    publish_url: str = Field(
        default="https://4nton.ai",
        description="The URL to publish artifacts generated by the Anton harness",
    )  # ANTON_PUBLISH_URL
    publish_ssl_verify: bool = Field(
        default=True,
        description="Whether to verify SSL certificates when publishing artifacts to the publish URL",
    )  # ANTON_SSL_VERIFY
    skills_root_dir: str = Field(
        default=str(Path.home() / ".cowork" / "anton" / "skills"),
        description="The root directory where the Anton harness will store skill-related files",
    )  # ANTON_SKILLS_ROOT_DIR